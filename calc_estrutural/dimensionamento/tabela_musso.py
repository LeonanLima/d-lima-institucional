TODAS_CHAVES = ("f", "ma", "mb", "mae", "mbe", "va", "vb", "vae", "vbe")


def _interp(vals, beta):
    """Interpolacao linear de uma lista alinhada a BETA_GRID, beta em [1,0;2,0]."""
    b = min(max(beta, BETA_GRID[0]), BETA_GRID[-1])
    for i in range(len(BETA_GRID) - 1):
        x0, x1 = BETA_GRID[i], BETA_GRID[i + 1]
        if x0 <= b <= x1:
            y0, y1 = vals[i], vals[i + 1]
            t = 0.0 if x1 == x0 else (b - x0) / (x1 - x0)
            return y0 + t * (y1 - y0)
    return vals[-1]


def coef_musso(tipo, beta):
    """Retorna todos os coeficientes do tipo interpolados em beta = b/a.

    Chaves ausentes no tipo retornam 0,0. beta e' clampado em [1,0; 2,0].
    """
    linha = TABELA_MUSSO.get(tipo, TABELA_MUSSO[1])
    return {k: (_interp(linha[k], beta) if k in linha else 0.0) for k in TODAS_CHAVES}
